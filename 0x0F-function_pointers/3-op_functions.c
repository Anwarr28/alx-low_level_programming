#include "3-calc.h"

/**
  * op_add - calculat the sum of two numbers.
  * @a: the first number.
  * @b: the second number.
  *
  * Return: the sum of the given numbers.
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - calculat the difference of two numbers.
  * @a: the first number.
  * @b: the second number.
  *
  * Return: the difference of the given numbers.
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - calculat the multiblication of two numbers.
  * @a: the first number.
  * @b: the second number.
  *
  * Return: the multiblication of the given numbers.
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - calculat the division of two numbers.
  * @a: the first number.
  * @b: the second number.
  *
  * Return: the division of the given numbers.
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - calculat the remainder of two numbers.
  * @a: the first number.
  * @b: the second number.
  *
  * Return: the remainder of the given numbers.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
